class Product {
  final String id;
  final String title;
  final String imageUrl;
  final double price;
  final double originalPrice;
  final String offer;
  final String deliveryTime;

  Product({
    required this.id,
    required this.title,
    required this.imageUrl,
    required this.price,
    required this.originalPrice,
    required this.offer,
    required this.deliveryTime,
  });
}
